<?php

/*
Auto incremento e auto decemento

- Podemos incrementar um valor ou decrementar com os operadores ++ e --;
- Exemplo: $n++ ou $x--;
- Onnde n e x são variáveis, e terão seus valores alterados com +1 e -1;
- Estes operadores são muito ultilizados em estruturas de repetição;
*/

$x = 0;
$y = 0;

$x++;
$y = $y +1;

echo "x: " . $x . "<br>";
echo "y: " . $y . "<br>";

$n = 10;
$m = 10;

$n--;
$m = $m -1;

echo "n: " . $n . "<br>";
echo "m: " . $m . "<br>";
