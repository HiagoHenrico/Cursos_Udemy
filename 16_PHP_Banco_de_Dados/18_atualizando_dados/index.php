 /* ATUALIZANDO DADOS
 
 - Para atualizar dados em uma tabela vamos utilizar a instrução UPDATE;
 - Precisamos determinar a tabela, colunas e valores;
 - Obs: Um UPDATE sem WHERE vai atualizar TODOS os dados da tabela;
 - Exemplo:
   UPDATE tabela 
   SET coluna1 = valor1
   WHERE condição;
 */